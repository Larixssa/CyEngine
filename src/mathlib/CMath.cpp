#include "CMath.h"


// ------------------------ ADDITION ------------------------ //

int CMath::add(int x, int y)
{
	return x + y;
}

float CMath::add(float x, float y)
{
	return x + y;
}

double CMath::add(double x, double y)
{
	return x + y;
}




// ------------------------ SUBTRACTION ------------------------ //

int CMath::sub(int x, int y)
{
	return x - y;
}

float CMath::sub(float x, float y)
{
	return x - y;
}

double CMath::sub(double x, double y)
{
	return x - y;
}




// ------------------------ MULTIPLICATION ------------------------ //

int CMath::mult(int x, int y)
{
	return x * y;
}

float CMath::mult(float x, float y)
{
	return x * y;
}

double CMath::mult(double x, double y)
{
	return x * y;
}




// ------------------------ DIVISION ------------------------ //

int CMath::div(int x, int y)
{
	return x / y;
}

float CMath::div(float x, float y)
{
	return x / y;
}

double CMath::div(double x, double y)
{
	return x / y;
}





// ------------------------ MISC ------------------------ //

float CMath::pi()
{
	return 3.14159;
}